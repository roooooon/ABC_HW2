#ifndef UNTITLED6_RND_H
#define UNTITLED6_RND_H

#include <cstdlib>

//------------------------------------------------------------------------------
// rnd.h - содержит генератор случайных чисел в диапазоне от 1 до 20
//------------------------------------------------------------------------------

inline auto Random() {
    return rand() % 20 + 1;
}
#endif //UNTITLED6_RND_H
